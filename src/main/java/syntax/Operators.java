package syntax;

public class Operators {

    public static void main(String[] args)
    {
        int a = 25;
        int b = 15;
        double c = 25;
        double d = 15;
        System.out.print("a + b = ");
        System.out.println(a + b);
        System.out.print("a - b = ");
        System.out.println(a - b);
        System.out.print("a * b = ");
        System.out.println(a * b);
        System.out.print("a / b = ");
        System.out.println(a / b);
        System.out.print("c / d = ");
        System.out.println(c / d);
        System.out.print("a % b = ");
        System.out.println(a % b);
        System.out.print("a == b = ");
        System.out.println(a == b);
        System.out.print("a != b = ");
        System.out.println(a != b);
        System.out.print("a > b = ");
        System.out.println(a > b);
        System.out.print("a < b = ");
        System.out.println(a < b);
        System.out.print("a >= b = ");
        System.out.println(a >= b);
        System.out.print("a <= b = ");
        System.out.println(a <= b);
        System.out.print("a += b = ");
        a += b;
        System.out.println(a);
        System.out.print("a -= b = ");
        a -= b;
        System.out.println(a);
        System.out.print("a *= b = ");
        a *= b;
        System.out.println(a);
        System.out.print("a /= b = ");
        a /= b;
        System.out.println(a);
        System.out.print("a %= b = ");
        a %= b;
        System.out.println(a);

    }
}
